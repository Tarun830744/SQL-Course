/*One of the built-in functions of MySQL (length)
Finds the size of the data under the specified Column*/

Select 'Tarun choudhary';
Select length('Tarun choudhary');
Select length('Tarun choudhary') AS GivenNameSize;

/*One of the built-in functions of MySQL (instr) 
Finds the position of the given text in the data of the specified Column*/

Select 'Tarun choudhary';
Select instr('Tarun choudhary','n');
Select instr('Tarun choudhary','n') AS Position;