/*One of the built-in functions of MySQL (substr)
Retrieves a portion of text from the data of the specified column*/

Select 'Tarun choudhary';
Select substr('Tarun choudhary',2,3);
Select substr('Tarun choudhary',2,3) AS Portion;
Select substr('Tarun choudhary',-3,2);
