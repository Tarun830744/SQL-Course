/*One of the built-in functions of MySQL (upper)
Converts the text under the specified Column data to Upper Case*/

Select upper('Tarun choudhary');
Select upper('Tarun choudhary') AS FullName;

/*One of the built-in functions of MySQL (lower)
Converts the text under the specified Column data to Lower Case*/

Select lower('Tarun choudhary');
Select lower('Tarun choudhary') AS FullName;